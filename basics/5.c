// unsigned vs signed integer variable types

void main(){
    char char_var = 5;
    unsigned char unsigned_char_var = 6; // 0 to 255
    signed char signed_char_var = -10;


    short short_var = -100;
    unsigned short unsigned_short_var = 100; // 0 to 300000 

    // 4 bytes
    int int_var = -10;
    unsigned int unsigned_int_var = 100; // 0 to 900000000


    long long_var = -10000000;
    unsigned long unsigned_long_var = 10000000; // 0 to 1000104309329090234895
}   